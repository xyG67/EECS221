package eecs221.Model;

public class WareHouse{
	private int aisle_X;
	private int aisle_Y;
	
	public WareHouse(int aisle_X, int aisle_Y){
		this.aisle_X = aisle_X;
		this.aisle_Y = aisle_Y;
	}

	public int getAisle_X() {
		return aisle_X;
	}

	public int getAisle_Y() {
		return aisle_Y;
	}
	
	
	
}
